using System;
using System.Collections.Generic;
using System.Text;

namespace Homework17_5
{
    public static class Calculator
        {
            public static dynamic Add(double a, double b)
            {
                return a + b;
            }
            public static dynamic Sub(double a, double b)
            {
                return a - b;
            }

            public static dynamic Mul(double a, double b)
            {
                return a * b;
            }

            public static dynamic Div(double a, double b)
            {
                return a / b;
            }
      }
    
}
